    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!studentCheck && !professorCheck){
           selectStatus();
        }
        else if(studentCheck) {
            boolean check;
            check = jdbc.checkStudentLogin(jTextField1.getText(), jTextField2.getText());
            if (check) {
                JFrameStudent studentFrame = new JFrameStudent();
                studentFrame.launch();
            } else {
                incorrectUP();
            }
        }
        else if(professorCheck) {
            boolean check = jdbc.checkProfessorLogin(jTextField1.getText(), jTextField2.getText());
            if(check) {
                dispose();
                JFrameProfessor professorFrame = new JFrameProfessor();
                professorFrame.launch();
            } else {
                incorrectUP();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


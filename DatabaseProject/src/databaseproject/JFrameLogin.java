    private void jTextField2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField2ActionPerformed
        String email = jTextField1.getText();
        String password = jTextField2.getText();

        if (jRadioButton1.isSelected()) {
            jLabel4.setText("You cannot sign up as a student, a professor must integrate you to the system manually");
            jLabel4.setVisible(true);
        } else if (jRadioButton2.isSelected()) {
            String professorId = JOptionPane.showInputDialog(this, "Enter Professor ID:");
            String firstName = JOptionPane.showInputDialog(this, "Enter First Name:");
            String lastName = JOptionPane.showInputDialog(this, "Enter Last Name:");
            String courseTaught = JOptionPane.showInputDialog(this, "Enter Course Taught:");

            if (professorId != null && firstName != null && lastName != null && courseTaught != null) {
                addProfessor(professorId, firstName, lastName, courseTaught, email, password);
            } else {
                jLabel4.setText("Professor sign up cancelled");
                jLabel4.setVisible(true);
            }
        } else {
            jLabel4.setText("Please select Student or Professor");
            jLabel4.setVisible(true);
        }
    }//GEN-LAST:event_jTextField2ActionPerformed

